class Calculator

  def self.add(num1, num2)
    return num1 + num2
  end

  def self.subtract(num1, num2)
    return num1 - num2
  end

  def self.multiply(num1, num2)
    return num1 * num2
  end

  def self.divide(num1, num2)
    return num1 / num2
  end

  def self.root(num1, num2)
    return num1**(1.0/num2)
  end

end
